#!/usr/bin/python3
for i in range(5):
	print(i)

#iterating in a specified start to end point
for i in range(5, 10):
	print(i)

#iteration in a specified start, end and increment value.
for i in range(0, 10, 3):
	print(i)

#iteration while negating sometimes called 'step'.
for i in range(-10, -100, -30):
	print(i)

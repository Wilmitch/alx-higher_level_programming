#!/usr/bin/python3
#Task to swap two numbers.
print("This program will switch the value of two variables that you will enter")
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
tmp = num1
num1 = num2
num2 = tmp
print(f"After the swap, First number becomes: {num1} and Second number becomes: {num2}")
print("Stay blessed!!!") 
